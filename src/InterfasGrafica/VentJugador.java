    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        if (validarCampos()){
            String nombre = this.txtNombre.getText();
            String alias = this.txtAlias.getText();
            int edad = Integer.parseInt(this.txtEdad.getText());
             Jugador unJugador = new Jugador(nombre,alias,edad);
            if (this.elJuego.unicoAlias(alias)){
                this.elJuego.setJugador(unJugador);
            }else{
                Iterator<Jugador> iter = this.elJuego.getJugadores().iterator();
                while( (iter.hasNext())){
                    Jugador aux = iter.next();
                    if (aux.equals(unJugador)) {
                        aux.setEdad(edad);
                        aux.setNombre(nombre);
                    }
                }
            }
            this.lblAviso.setText("Jugador: "+this.txtAlias.getText()+" .Ingresado correctamente.");
            this.limpiarCampos();
        } 
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private boolean validarCampos(){
        boolean ret = true;
        //Limpiamos las lblÂ´s
        lbl_errNombre.setText("");
        lbl_errAlias.setText("");
        lbl_errEdad.setText("");
        lblAviso.setText("");
        
        //Validamos campos.
        if (this.txtNombre.getText().equals("")) {
            ret = false;
            lbl_errNombre.setText("Debe ingresar un nombre.");
        }
        if (this.txtAlias.getText().equals("")){
            ret = false;
            lbl_errAlias.setText("Debe ingresar un alias.");
        }
        if (this.txtEdad.getText().equals("")){
            ret = false;
            lbl_errEdad.setText("Debe ingresar una edad.");
        }
        if (Integer.parseInt(this.txtEdad.getText())< 1 ||  Integer.parseInt(this.txtEdad.getText())> 150) {
            ret = false;
            lbl_errEdad.setText("Debe ingresar una edad coherente");
        }
        return ret;
    }
    private void limpiarCampos(){
        this.txtAlias.setText("");
        this.txtNombre.setText("");
        this.txtEdad.setText("");
    }
